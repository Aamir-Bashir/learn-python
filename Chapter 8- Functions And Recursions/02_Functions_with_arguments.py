#There are functions which are parameterized 
#For Example
def GoodDay(name , ending): #Here name and ending are the parameters that we have given to the function definition
    print("Good Day, " + name)
    print(ending)

GoodDay("Ahmed", "Thankyou")
GoodDay("Ali", "Thankyou")
GoodDay("Aamir", "Thankyou")
GoodDay("Ayan", "Thankyou")