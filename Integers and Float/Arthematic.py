num = 10            # this is an integer variable
num_2 = 6.78         # this is a float variable

print(type(num))      # prints the type of num
print(type(num_2))    # prints the type of num_2

# Arithmetic operations
print(num + num_2)    # addition
print(num - num_2)    # subtraction 
print(num * num_2)    # multiplication
print(num / num_2)    # division
print(num // num_2)   # floor division
print(num % num_2)    # modulus
print(num ** num_2)   # exponentiation
print(num + 5)        # addition with an integer
print(num_2 + 2.78)   # addition with a float
print(num + num_2 + 5)  # this adds both variables and an integer
print(num * (5 + 3))  # this applies the rules of BODMAS

#arthmetic functions
print(abs(-num))      # absolute value
print(round(num_2))   # rounds the float to the nearest integer
print(pow(num, 2))    # raises num to the power of 2
