#Sets are unordered collections of unique elements.
#They are defined using curly braces {} or the set() function.
#empty set
S = set()  # this is an empty set , dont use {} as it creates an empty dictionary
print(type(S))

s = {1, 2, 3, 4, 5,5,5,5}  # this is a set with unique elements
print(s)  # Output: {1, 2, 3, 4, 5} , duplicates are removed
